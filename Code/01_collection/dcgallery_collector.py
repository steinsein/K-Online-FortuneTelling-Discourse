import pandas as pd
import requests
from bs4 import BeautifulSoup
import time
from tqdm import tqdm
import os

def get_post_content(url, headers, retries=3, delay=2):
    """
    주어진 URL에서 게시글 본문을 추출합니다.
    오류 발생 시 지정된 횟수만큼 재시도합니다.
    """
    for attempt in range(retries):
        try:
            # 타임아웃을 10초로 설정하여 무한 대기를 방지합니다.
            response = requests.get(url, headers=headers, timeout=10)
            # 4xx 또는 5xx 같은 HTTP 오류 코드가 반환되면 예외를 발생시킵니다.
            response.raise_for_status()
            soup = BeautifulSoup(response.text, 'html.parser')

            # 1. 본문 내용이 담긴 핵심 영역을 선택합니다.
            content_box = soup.select_one('div.gallview_contents div.writing_view_box')

            if content_box:
                # 2. 요소 내부의 텍스트를 추출하고, 줄바꿈으로 정리하여 반환합니다.
                return content_box.get_text(separator='\n', strip=True)
            else:
                return "본문을 찾을 수 없습니다."

        except requests.exceptions.RequestException as e:
            # 네트워크 관련 오류 발생 시 (너무 많은 출력 방지를 위해 콘솔 출력 최소화)
            if attempt < retries - 1:
                time.sleep(delay)
            else:
                return f"오류 발생: {e}" # 마지막 시도 실패 시 오류 메시지 반환

        except Exception as e:
            # 기타 예상치 못한 오류 발생 시
            return f"기타 오류: {e}" # 재시도 없이 즉시 오류 메시지 반환

def process_keyword_csv(keyword, chunk_size=5000):
    """특정 키워드에 대한 CSV 파일을 읽어 본문을 추가하고 분할 저장합니다."""

    input_file_path = f'디씨역학_{keyword}_게시글.csv'
    output_file_base_path = f'디씨역학_{keyword}_게시글_본문추가'

    # 해당 키워드의 원본 파일이 있는지 먼저 확인
    if not os.path.exists(input_file_path):
        print(f"⚠️ 건너뜀: '{input_file_path}' 파일을 찾을 수 없습니다.")
        return

    print(f"\n============================================================")
    print(f" 🚀 현재 진행 중인 키워드: '{keyword}'")
    print(f"============================================================")

    try:
        # 1. CSV 파일을 DataFrame으로 불러옵니다.
        df = pd.read_csv(input_file_path)

        # 2. 서버 차단을 피하기 위한 User-Agent 헤더
        headers = {
            'User-Agent': 'Mozilla/5.0 (Windows NT 10.0; Win64; x64) AppleWebKit/537.36 (KHTML, like Gecko) Chrome/91.0.4472.124 Safari/537.36'
        }

        total_rows = len(df)
        print(f"총 {total_rows}개의 게시글에서 본문 추출을 시작합니다...")

        # 3. 데이터를 지정된 개수(chunk_size) 단위로 나누어 처리합니다.
        for i in range(0, total_rows, chunk_size):
            start_index = i
            end_index = min(i + chunk_size, total_rows)
            df_chunk = df.iloc[start_index:end_index].copy() # Warning 방지를 위해 copy() 사용

            part_num = (i // chunk_size) + 1
            output_file_path = f"{output_file_base_path}_part_{part_num}.csv"

            # [이어하기 기능] 이미 생성된 파일이 있다면 스크래핑 생략
            if os.path.exists(output_file_path):
                print(f"\nℹ️ 알림: '{output_file_path}' 파일이 이미 존재하여 건너뜁니다.")
                continue

            print(f"\n--- Part {part_num} 처리 중 ({start_index+1} ~ {end_index} 행) ---")

            # 4. 각 링크를 순회하며 본문 내용을 담을 리스트를 생성합니다.
            content_list = []

            # tqdm을 사용하여 진행 상황을 시각적으로 표시합니다.
            for url in tqdm(df_chunk['링크'], desc=f"Part {part_num} 본문 추출 중"):
                content = get_post_content(url, headers)
                content_list.append(content)
                # 서버에 부담을 주지 않기 위해 약간의 지연 시간을 줍니다.
                time.sleep(0.1)

            # 5. DataFrame 조각(chunk)에 '본문' 열을 추가합니다.
            df_chunk['본문'] = content_list

            # 6. 현재까지 처리된 조각(chunk)을 별도의 CSV 파일로 저장합니다.
            df_chunk.to_csv(output_file_path, index=False, encoding='utf-8-sig')
            print(f"✅ Part {part_num} 본문 추출 완료! '{output_file_path}' 파일에 저장되었습니다.")

    except Exception as e:
        print(f"❌ 스크립트 실행 중 오류가 발생했습니다 [{keyword}]: {e}")


