import requests
from bs4 import BeautifulSoup
import pandas as pd
import time
from urllib.parse import quote # URL 인코딩을 위해 import

def get_posts_from_page(url, headers):
    """
    지정된 URL 페이지에서 게시글 정보를 추출하고, 2018년 중단 조건을 확인합니다.
    (재시도 로직 포함)

    반환값: (page_data, stop_scraping)
           page_data: 수집된 게시글 데이터 리스트
           stop_scraping: 2018년도 게시글 발견 시 True
    """
    max_retries = 3 # 최대 3번까지 재시도
    retry_delay = 3   # 재시도 전 3초 대기

    for attempt in range(max_retries):
        try:
            # 타임아웃(10초)을 설정하여 무한 대기 방지
            response = requests.get(url, headers=headers, timeout=10)
            # HTTP 오류 코드가 반환되면 예외 발생
            response.raise_for_status()

            soup = BeautifulSoup(response.text, 'html.parser')
            # ⭐️ 공지사항(ub-content notice) 제외하고 일반 게시글만 선택
            post_list = soup.select('tr.ub-content.us-post:not(.notice)')

            if not post_list:
                return [], False # 게시글 없음, 중단 아님

            page_data = []
            stop_scraping = False # 2018년 중단 플래그

            for post in post_list:
                try:
                    num = post.select_one('.gall_num').text.strip()
                    # 숫자가 아닌 경우(광고 등) 건너뜀
                    if not num.isdigit():
                        continue

                    date_tag = post.select_one('.gall_date')
                    # title 속성이 없을 경우 text 사용 (YYYY.MM.DD 형식)
                    full_datetime = date_tag.get('title', date_tag.text.strip())
                    # datetime 형식에서 날짜 부분만 추출
                    date = full_datetime.split(' ')[0]

                    # ⭐️ 2018년 중단 조건 확인
                    year_str = date.split('.')[0]
                    if year_str.isdigit() and len(year_str) == 4:
                        year = int(year_str)
                        if year <= 2018:
                            print(f"\n[중단 조건 발견] {year}년 게시글({date})을 발견하여 스크래핑을 중단합니다.")
                            stop_scraping = True
                            break # 현재 페이지의 나머지 게시글 처리를 중단

                    # 2018년이 아니면 데이터 추출 계속
                    title_tag = post.select_one('.gall_tit a:not(.icon_pic):not(.icon_survey):not(.icon_voice)') # 아이콘 제외
                    if not title_tag:
                        continue

                    title = title_tag.text.strip()
                    # 댓글 수 태그 (em.reply_num)가 있으면 제거
                    reply_num_tag = title_tag.select_one("em.reply_num")
                    if reply_num_tag:
                        title = title.replace(reply_num_tag.text, '').strip() # 댓글 수 부분 제거

                    link = "https://gall.dcinside.com" + title_tag['href']

                    writer_tag = post.select_one('.gall_writer')
                    nickname = writer_tag.get('data-nick', '').strip()
                    ip = writer_tag.get('data-ip', '').strip()
                    writer = f"{nickname}({ip})" if ip else nickname

                    count = post.select_one('.gall_count').text.strip()
                    recommend = post.select_one('.gall_recommend').text.strip()

                    page_data.append([num, title, writer, date, count, recommend, link])

                except (AttributeError, KeyError, IndexError) as e:
                    # print(f"   - 특정 게시글 처리 중 오류 발생: {e}, 건너뜁니다.")
                    continue

            # 성공적으로 데이터를 가져왔다면 결과를 반환하고 함수 종료
            return page_data, stop_scraping

        # requests 관련 예외가 발생했을 때 실행
        except requests.exceptions.RequestException as e:
            print(f"페이지 요청 중 오류 발생 (시도 {attempt + 1}/{max_retries}): {e}")
            if attempt < max_retries - 1:
                print(f"{retry_delay}초 후 재시도합니다...")
                time.sleep(retry_delay)
            else:
                print("최종 접속에 실패했습니다.")

    # 모든 재시도가 실패하면 (빈 리스트, 중단 아님)을 반환
    return [], False

def scrape_keyword_data(keyword):
    """특정 키워드 하나에 대한 스크래핑을 수행하는 함수"""

    # URL에서 가져온 시작 search_pos 및 갤러리 ID
    search_pos = -17255549
    gallery_id = "divination"

    # URL 인코딩된 키워드 사용
    encoded_keyword = quote(keyword)
    base_url = f"https://gall.dcinside.com/board/lists/?id={gallery_id}&s_type=search_subject_memo&s_keyword={encoded_keyword}"

    headers = {
        'User-Agent': 'Mozilla/5.0 (Windows NT 10.0; Win64; x64) AppleWebKit/537.36 (KHTML, like Gecko) Chrome/91.0.4472.124 Safari/537.36'
    }

    all_posts = []
    current_search_pos = search_pos
    stop_all_scraping = False # 2018년 발견 시 전체 중단을 위한 플래그

    # search_pos가 음수인 동안, 그리고 중단 플래그가 False인 동안 계속 반복
    while current_search_pos < 0 and not stop_all_scraping:
        print(f"\n  [검색 단위 이동] search_pos: {current_search_pos}")

        page = 1
        processed_post_ids = set() # 현재 search_pos 내에서 처리된 게시글 번호 저장

        while not stop_all_scraping: # 2018년 발견 시 이 루프도 중단됨
            url_to_scrape = f"{base_url}&page={page}&search_pos={current_search_pos}"

            print(f"  - 스크래핑 중 (Page: {page})... ", end="") # 줄바꿈 없이 출력

            # ⭐️ 2018년 중단 플래그(page_stop_flag)를 함께 반환받음
            posts_on_page, page_stop_flag = get_posts_from_page(url_to_scrape, headers)

            # 1. 2018년 중단 조건이 감지된 경우
            if page_stop_flag:
                stop_all_scraping = True
                if posts_on_page:
                    print(f"{len(posts_on_page)}개 게시글 추가 후 중단.")
                    all_posts.extend(posts_on_page)
                break

            # 2. 페이지에 게시글이 없는 경우
            if not posts_on_page:
                print("게시글 없음. 현재 검색 단위 종료.")
                break

            # 3. 중복 페이지 감지
            current_page_ids = {post[0] for post in posts_on_page}
            if current_page_ids.issubset(processed_post_ids):
                 print(f"중복 페이지 감지됨. 현재 검색 단위 종료.")
                 break

            # 4. 새로운 게시글만 필터링하여 추가
            new_posts = []
            for post in posts_on_page:
                post_id = post[0]
                if post_id not in processed_post_ids:
                    new_posts.append(post)
                    processed_post_ids.add(post_id)

            if new_posts:
                print(f"{len(new_posts)}개 신규 수집.")
                all_posts.extend(new_posts)
            else:
                print("신규 게시글 없음. 현재 검색 단위 종료.")
                break

            page += 1
            time.sleep(0.7) # 서버 부하 감소

        # 다음 search_pos 블록으로 이동 (+10000)
        current_search_pos += 10000

    if stop_all_scraping:
        print(f"  ▶ 2018년 이전 게시글 도달. '{keyword}' 스크래핑 최종 중단.")
    else:
        print(f"  ▶ search_pos가 0 이상이 되어 '{keyword}' 스크래핑 최종 종료.")

    return all_posts


