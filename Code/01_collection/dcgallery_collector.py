import pandas as pd
import os
import glob
import re

def combine_csv_for_keyword(keyword):
    """
    특정 키워드가 포함된 모든 part 파일을 찾아 하나로 병합 후 저장합니다.
    """
    # 1. 키워드가 포함되고 'part_'로 끝나는 모든 CSV 파일 찾기
    # 예: 폴더 내의 '*타로*part_*.csv' 에 해당하는 모든 파일을 찾습니다.
    file_pattern = f"*{keyword}*part_*.csv"
    file_paths = glob.glob(file_pattern)

    # 2. 해당 키워드의 part 파일이 없으면 다음 키워드로 넘어감
    if not file_paths:
        print(f"⚠️ '{keyword}' 키워드에 해당하는 파일이 없습니다. 건너뜁니다.")
        return

    # 3. 파일 이름에서 part_ 뒤의 숫자를 추출하여 올바른 순서(1, 2, 3...)로 정렬
    # (일반 정렬을 하면 part_10 이 part_2 보다 앞에 오기 때문에 숫자로 정렬)
    try:
        file_paths.sort(key=lambda x: int(re.search(r'part_(\d+)', x).group(1)))
    except Exception:
        file_paths.sort() # 만약 정규식 추출에 실패하면 기본 이름순 정렬

    print(f"\n▶ [{keyword}] 파일 병합 시작 (총 {len(file_paths)}개 파일 발견)")

    all_dataframes = []

    # 4. 정렬된 각 파일을 순서대로 읽어서 리스트에 추가
    for file_path in file_paths:
        try:
            df = pd.read_csv(file_path)
            all_dataframes.append(df)
            print(f"  - 로드 성공: {file_path}")
        except Exception as e:
            print(f"  - ❌ 로드 실패 ({file_path}): {e}")

    # 5. 데이터프레임 병합 및 새 파일로 저장
    if all_dataframes:
        combined_df = pd.concat(all_dataframes, ignore_index=True)

        # 저장할 통합 파일 이름 설정
        output_file_path = f'디씨역학_{keyword}_본문추가.csv'

        try:
            combined_df.to_csv(output_file_path, index=False, encoding='utf-8-sig')
            print(f"✅ [{keyword}] 병합 완료!")
            print(f"  - 저장 파일명: {output_file_path}")
            print(f"  - 총 데이터 수: {len(combined_df)}행")
        except Exception as e:
            print(f"❌ 파일 저장 중 오류 발생: {e}")
    else:
        print(f"[{keyword}] 병합할 데이터가 없습니다.")


