# 1. 날짜 형식 변환 함수
def format_date(date_string):
    """
    fmkorea의 날짜 형식을 'YYYY.MM.DD'로 변환합니다.
    - 'HH:MM' 형식은 오늘 날짜로 변환
    - 'YY.MM.DD' 형식은 '20YY.MM.DD'로 변환
    """
    try:
        if ':' in date_string:  # '13:05'와 같은 시간 형식
            return datetime.now().strftime('%Y.%m.%d')
        elif date_string.count('.') == 2:  # '25.11.10' 또는 '2025.11.10'
            parts = date_string.split('.')
            if len(parts[0]) == 2:  # 'YY' 형식
                return f"20{parts[0]}.{parts[1]}.{parts[2]}"
            return date_string
        else:
            return date_string
    except Exception:
        return date_string

# 2. CSV 저장 함수 (카테고리명 반영)
def save_to_csv(data, category, keyword):
    """
    수집된 데이터를 CSV 파일로 저장합니다.
    파일 이름: '에펨코리아_카테고리명_키워드.csv'
    """
    # 파일명에 사용할 수 없는 문자가 카테고리명에 있을 경우 언더스코어로 대체
    safe_category = category.replace("/", "_").replace("\\", "_")
    filename = f"에펨코리아_{safe_category}_{keyword}.csv"
    headers = ['번호', '제목', '링크', '글쓴이', '작성일', '조회']

    try:
        with open(filename, 'w', encoding='utf-8-sig', newline='') as f:
            writer = csv.writer(f)
            writer.writerow(headers)
            writer.writerows(data)
        print(f"\n✅ 성공: '{filename}' 파일로 저장이 완료되었습니다.")
    except Exception as e:
        print(f"\n❌ 오류: 파일 저장 중 문제가 발생했습니다. {e}")

# 3. 핵심 스크래핑 함수
def scrape_keyword(driver, category_name, board_id, keyword):
    """
    (Selenium) 새 HTML 구조(테이블)에 맞춰 검색하고 페이징하며 스크래핑합니다.
    """
    scraped_links = set()
    all_results = []
    page = 1
    encoded_keyword = urllib.parse.quote(keyword)

    print(f"\n--- [{category_name}] 게시판에서 '{keyword}' 키워드 검색 및 스크래핑 시작 ---")

    while True:
        # URL에 제공받은 게시판 ID(mid) 적용
        target_url = f"https://www.fmkorea.com/search.php?mid={board_id}&search_keyword={encoded_keyword}&search_target=title_content&page={page}"
        print(f"Scraping page {page}: {target_url}")

        try:
            driver.get(target_url)
            time.sleep(1.5) # 페이지 로드를 위해 잠시 대기

            soup = BeautifulSoup(driver.page_source, 'html.parser')
            posts = soup.select("table.bd_lst > tbody > tr")

            if not posts:
                print(f"Page {page}: 데이터가 없습니다. 스크래핑을 종료합니다.")
                break

            new_data_found_on_this_page = False
            page_results = []

            for post in posts:
                try:
                    post_classes = post.get('class', [])
                    if 'notice' in post_classes or 'ad_wrapper' in post_classes:
                        continue

                    title_element = post.select_one("td.title > a.hx")
                    author_element = post.select_one("td.author a")
                    date_element = post.select_one("td.time")
                    m_no_elements = post.select("td.m_no")
                    views_element = m_no_elements[0] if m_no_elements else None

                    if not title_element or not author_element or not date_element or not views_element:
                        continue

                    link = title_element['href']
                    if not link.startswith('http'):
                        link = "https://www.fmkorea.com" + link

                    if link in scraped_links:
                        continue

                    new_data_found_on_this_page = True
                    scraped_links.add(link)

                    parsed_url = urlparse(link)
                    query_params = parse_qs(parsed_url.query)
                    number = query_params.get('document_srl', [None])[0]

                    if number is None:
                        number = link.split('/')[-1].split('?')[0]

                    title = title_element.text.strip()
                    author = author_element.text.strip()
                    date_raw = date_element.text.strip()
                    date = format_date(date_raw)
                    views = views_element.text.strip().replace(',', '')

                    page_results.append([number, title, link, author, date, views])

                except Exception as e:
                    print(f"  - (오류) 게시물 1개 파싱 중 문제 발생: {e}")

            if not new_data_found_on_this_page and page > 1:
                 print(f"Page {page}: 새로운 데이터가 없고 중복 데이터만 감지되었습니다. 스크래핑을 종료합니다.")
                 break

            if not page_results and not new_data_found_on_this_page:
                print(f"Page {page}:에서 추가 데이터를 찾지 못했습니다. 스크래핑을 종료합니다.")
                break

            all_results.extend(page_results)
            page += 1

        except Exception as e:
            print(f"❌ (오류) Page {page} 파싱 중 예기치 못한 문제 발생: {e}")
            break

    if all_results:
        save_to_csv(all_results, category_name, keyword)
    else:
        print(f"\n--- [{category_name}] 게시판에서 '{keyword}'에 대한 검색 결과가 없습니다. ---")


