def get_content_from_link(driver, link):
    """Selenium 드라이버로 개별 링크에 접속하여 본문 내용을 스크래핑합니다."""
    try:
        driver.get(link)
        time.sleep(1.5)
        post_soup = BeautifulSoup(driver.page_source, 'html.parser')
        content_element = post_soup.select_one("div.rd_body .xe_content")

        if content_element:
            return content_element.text.strip().replace('\n', ' ')
        else:
            return "본문 수집 실패"
    except WebDriverException as e:
        return "WebDriver 오류"
    except Exception as e:
        return "본문 수집 오류"

def process_csv_for_content(driver, category_name, keyword):
    """저장된 목록 CSV를 읽고 본문을 추가하여 새 파일로 저장합니다."""
    input_filename = get_safe_filename(category_name, keyword)
    output_filename = get_safe_filename(category_name, keyword, "+본문")

    if os.path.exists(output_filename):
        print(f"  ℹ️ [본문 수집 패스] '{output_filename}' 파일이 이미 존재합니다.")
        return

    original_data = []
    headers = []

    try:
        with open(input_filename, 'r', encoding='utf-8-sig') as f:
            reader = csv.reader(f)
            headers = next(reader)
            original_data = list(reader)
    except FileNotFoundError:
        return # 목록 파일이 없는 경우 패스
    except Exception as e:
        print(f"❌ '{input_filename}' 파일 읽기 오류: {e}")
        return

    try:
        link_column_index = headers.index("링크")
    except ValueError:
        print(f"❌ '{input_filename}'에 '링크' 컬럼이 없습니다.")
        return

    print(f"▶ [{category_name}] '{keyword}' 본문 추출 시작 ({len(original_data)}개)")

    new_data = [headers + ["본문"]]

    for i, row in enumerate(tqdm(original_data, desc="본문 수집 중")):
        link_to_visit = row[link_column_index]
        content = get_content_from_link(driver, link_to_visit)
        new_data.append(row + [content])
        time.sleep(0.5) # 과부하 방지

    if len(new_data) > 1:
        try:
            with open(output_filename, 'w', encoding='utf-8-sig', newline='') as f:
                writer = csv.writer(f)
                writer.writerows(new_data)
            print(f"✅ [본문 저장 완료] '{output_filename}'")
        except Exception as e:
            print(f"❌ '{output_filename}' 파일 저장 오류: {e}")

