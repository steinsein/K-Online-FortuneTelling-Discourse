# [함수] 스마트 도넛 차트 그리기 (겹침 방지 + 범례 강화)
# ------------------------------------------------------------------
def plot_smart_donut(ax, data_series, title, colors_dict):
    # 1. 데이터 정렬 (큰 순서대로)
    data = data_series.sort_values(ascending=False)

    # 2. 색상 매핑
    colors = [colors_dict.get(x, '#999999') for x in data.index]

    # 3. 차트 그리기 (라벨은 일단 None으로 설정하여 자동 출력을 막음)
    wedges, texts, autotexts = ax.pie(
        data,
        labels=None,  # ★ 겹침 방지를 위해 여기서 라벨을 끄고 범례로 대체합니다
        autopct='%1.1f%%',
        startangle=90,
        counterclock=False, # 시계 방향으로 정렬
        colors=colors,
        pctdistance=0.85,
        wedgeprops={'edgecolor': 'white', 'linewidth': 2}
    )

    # 4. 도넛 모양 만들기
    centre_circle = plt.Circle((0,0), 0.60, fc='white')
    ax.add_artist(centre_circle)

    # 5. [핵심] 작은 파이 조각의 % 텍스트 숨기기 (겹침 방지)
    threshold = 3.0 # 3% 미만은 그래프 위에서 글자 숨김
    for i, p in enumerate(wedges):
        ang = (p.theta2 - p.theta1) / 360 * 100
        if ang < threshold:
            autotexts[i].set_visible(False) # % 텍스트 숨기기
        else:
            autotexts[i].set_fontsize(18)
            autotexts[i].set_fontweight('bold')
            autotexts[i].set_color('white') # 가독성을 위해 흰색 글씨

    # 6. 타이틀 설정
    ax.set_title(title, fontsize=22, fontweight='bold')

    # 7. [핵심] 우측 범례(Legend) 생성
    # 범례에 "이름 : 00.0%" 형태로 표시
    total = data.sum()
    legend_labels = [f'{label} : {val/total*100:.1f}%' for label, val in zip(data.index, data)]

    ax.legend(
        wedges,
        legend_labels,
        title="플랫폼 비율",
        loc="center left",
        bbox_to_anchor=(0.9, 0.5), # 그래프 오른쪽 바깥에 배치
        fontsize=18
    )

