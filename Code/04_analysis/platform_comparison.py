def analyze_emotion_distribution(dataframe, channels, keywords_dict):
    results = []
    for channel in channels:
        subset = dataframe[dataframe['channel_type'] == channel]
        text_blob = ' '.join(subset['whole_content'].tolist())
        total_length = len(text_blob)

        display_name = legend_mapping.get(channel, channel)

        # 데이터가 없을 경우 0으로 채움
        if total_length == 0:
            row = {'Channel': display_name}
            for k in keywords_dict.keys(): row[k] = 0
            results.append(row)
            continue

        row = {'Channel': display_name}
        for category, words in keywords_dict.items():
            count = sum(text_blob.count(w) for w in words)
            # 텍스트 길이에 대비한 출현 빈도 (정규화 스코어)
            normalized_score = (count / total_length) * 10000
            row[category] = normalized_score

        results.append(row)

    return pd.DataFrame(results).set_index('Channel')

