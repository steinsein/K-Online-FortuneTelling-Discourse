# [Step 2] AI 서비스 키워드 매핑
# ==============================================================================
# (참고: 소문자로 변환해서 검사하므로 'GPT', 'Gpt' 같은 중복은 제거해도 무방합니다)
target_ai_keywords = {
    'GPT': ['gpt', '지피티', '챗지피티', 'chatgpt'],
    '뤼튼': ['뤼튼', 'wrtn'],
    'Claude': ['claude', '클로드'],
    'Gemini': ['gemini', '제미나이', '재미나이'],
    'Grok': ['grok', '그록'],
    '기타': ['ai 사주', 'ai사주', 'ai운세', 'ai 운세', 'ai타로', 'ai 타로', 'ai 신점', 'ai신점']
}


def extract_ai_keyword(text):
    text_lower = str(text).lower()
    found_ais = []

    for ai_name, keywords in target_ai_keywords.items():
        for kw in keywords:
            if kw == 'ai':
                # 'email', 'again' 등 영문 단어 안의 ai는 제외하고, 순수 ai(또는 한글이 붙은 ai)만 매칭
                if re.search(r'(?<![a-z])ai(?![a-z])', text_lower):
                    found_ais.append(ai_name)
                    break
            else:
                if kw.lower() in text_lower:
                    found_ais.append(ai_name)
                    break

    if not found_ais:
        return None

    # 여러 개가 검출되면 사전에 정의된 순서(GPT > 뤼튼 > ...)대로 가장 첫 번째를 반환
    return found_ais[0]

