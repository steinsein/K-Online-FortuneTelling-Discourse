# ==============================================================================
def load_and_preprocess_ai_data(file_path):
    df = pd.read_csv(file_path)
    df['whole_content'] = df['whole_content'].fillna('').astype(str)

    # 1. 날짜 데이터
    df['date'] = pd.to_datetime(df['작성날짜'], errors='coerce')

    # ★ 2022년 11월 1일 이후 데이터만 남기기
    df = df[df['date'] >= '2022-11-01']
    print(f"✅ 2022년 11월 이후 분석 대상 데이터: {len(df):,}건")

    # 2. 분기(Quarter) 단위 변환
    df['Period'] = df['date'].dt.to_period('Q')

    # 3. 사이트 칼럼을 Platform으로 복사
    if 'Platform' not in df.columns:
        df['Platform'] = df['사이트']

    # '기타' 플랫폼 제외
    df = df[df['Platform'] != '기타']

    # 4. AI 서비스 키워드 추출
    target_ai_keywords = {
        'GPT': ['gpt', 'Gpt', '지피티', 'GPT', '챗지피티', 'chatgpt'],
        '뤼튼': ['뤼튼', 'wrtn'],
        'Claude': ['Claude', '클로드'],
        'Gemini': ['Gemini', '제미나이', '재미나이'],
        'Grok': ['Grok', '그록'],
        '기타': ['ai 사주', 'ai사주', 'ai운세', 'ai 운세', 'ai타로', 'ai 타로', 'ai 신점', 'ai신점']
    }

    def extract_ai_keyword(text):
        text_lower = str(text).lower()
        found_ais = []
        for ai_name, keywords in target_ai_keywords.items():
            for kw in keywords:
                if kw.lower() in text_lower:
                    found_ais.append(ai_name)
                    break
        if not found_ais:
            return None
        return found_ais[0]

    print(">>> AI 키워드 추출 중...")
    df['AI_Service'] = df['whole_content'].apply(extract_ai_keyword)

    # AI 키워드가 언급된 데이터만 필터링
