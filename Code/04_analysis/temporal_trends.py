    POINT_COLOR = color_code
    FILL_COLOR = color_code

    services = data_df.index.tolist()
    num_plots = len(services)

    if num_plots == 0:
        return

    # 행/열 계산 (3열 고정)
    cols = 3
    rows = math.ceil(num_plots / cols)

    fig, axes = plt.subplots(rows, cols, figsize=(15, 4 * rows), facecolor='white')

    # 제목이 있을 때만 표시
    if main_title:
        fig.suptitle(main_title, fontsize=30, fontweight='bold', y=1.00 + (0.02/rows))

    if num_plots > 1:
        axes = axes.flatten()
    else:
        axes = [axes]

    for idx, ax in enumerate(axes):
        if idx < num_plots:
            service = services[idx]
            row_data = data_df.iloc[idx].values
            numeric_data = pd.to_numeric(row_data, errors='coerce')
            x = np.arange(len(numeric_data))

            mask = ~np.isnan(numeric_data)
            valid_x = x[mask]
            valid_data = numeric_data[mask]

