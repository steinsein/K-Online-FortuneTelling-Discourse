def load_and_prepare(file_path):
    df = pd.read_csv(file_path)

    # 1. 날짜 데이터 (필터링 생략)
    df['date'] = pd.to_datetime(df['작성날짜'], errors='coerce')

    # 2. 분기(Quarter) 단위 변환
    df['Period'] = df['date'].dt.to_period('Q')

    # 3. 사이트 칼럼을 Platform으로 복사
    if 'Platform' not in df.columns:
        df['Platform'] = df['사이트']

    # ★ 핵심 보완: 네이버카페 AI/챗봇 (2022.11 이전) 데이터를 '기타어플'로 재분류
    change_condition = (
        (df['Platform'] == '네이버카페') &
        (df['channel_type'] == 'AI(ChatGPT/챗봇 등)') &
        (df['date'] < '2022-11-01')
    )

    changed_count = change_condition.sum()
    df.loc[change_condition, 'channel_type'] = '기타어플'

    print(f"🔄 네이버카페 AI/챗봇 (2022.11 이전) '기타어플'로 재분류: {changed_count:,}건")

    # X축(분기) 고정을 위한 시작/종료일 설정
    start_date = "2019-01-01"
    end_date = "2025-09-30"

    return df, start_date, end_date

# ==============================================================================
# [Step 2] 분기별 집계 데이터 변환 (지정된 순서 유지)
# ==============================================================================
def prepare_quarterly_data(df_platform, column_name, start_date, end_date, target_list):
    # 1. 먼저 대상 항목으로만 데이터 필터링 (레이아웃 순서 유지용)
    df_filtered = df_platform[df_platform[column_name].isin(target_list)]

    # 2. 분기별/항목별 집계
    quarterly_counts = df_filtered.groupby([column_name, 'Period']).size().unstack(fill_value=0)

    # 3. 기간 내 모든 분기를 컬럼으로 확보 (빈 분기는 0 채우기)
    all_quarters = pd.period_range(start=start_date, end=end_date, freq='Q')
    quarterly_counts = quarterly_counts.reindex(columns=all_quarters, fill_value=0)

    # 4. 행(Index)을 target_list 순서대로 고정 (데이터가 없어도 패널 유지를 위해 0으로 추가)
    final_df = quarterly_counts.reindex(target_list, fill_value=0)

    return final_df

