from matplotlib.gridspec import GridSpec

def plot_horizontal_trends(data_df, platform_colors, output_path):
    platforms = data_df.index.tolist()

    # 전체 캔버스 설정 (상하 2행 배치를 위해 세로 길이를 대폭 늘림)
    fig = plt.figure(figsize=(24, 14), constrained_layout=True)

    # GridSpec 설정: 상단 1.2, 하단 1.0 비율로 2행 6열 생성
    gs = GridSpec(2, 6, figure=fig, height_ratios=[1.2, 1.0])

    # 상단 2개 그래프 (각각 3열씩 넓게 차지)
    ax1 = fig.add_subplot(gs[0, 0:3])
    ax2 = fig.add_subplot(gs[0, 3:6])

    # 하단 3개 그래프 (각각 2열씩 차지)
    ax3 = fig.add_subplot(gs[1, 0:2])
    ax4 = fig.add_subplot(gs[1, 2:4])
    ax5 = fig.add_subplot(gs[1, 4:6])

    axes = [ax1, ax2, ax3, ax4, ax5]

    for idx, (ax, platform) in enumerate(zip(axes, platforms)):
        color_code = platform_colors.get(platform, '#999999')

        row_data = data_df.iloc[idx].values
        numeric_data = pd.to_numeric(row_data, errors='coerce')
        x = np.arange(len(numeric_data))

        mask = ~np.isnan(numeric_data)
        valid_x = x[mask]
        valid_data = numeric_data[mask]

        # 상단과 하단 그래프의 폰트 크기를 다르게 설정
        if idx < 2:  # 상단 그래프
            title_fs = 31      # 하단 플랫폼 이름 폰트
            anno_fs = 24       # 최고/최저점 숫자 폰트
            tick_fs = 21       # X축 연도 폰트
            line_w = 5         # 상단은 선 굵기도 살짝 두껍게
        else:        # 하단 그래프
            title_fs = 25
            anno_fs = 20
            tick_fs = 19
            line_w = 4

        # 스플라인 곡선 (부드럽게 표현)
        if len(valid_data) > 3:
            try:
                x_smooth = np.linspace(valid_x.min(), valid_x.max(), 300)
                spl = make_interp_spline(valid_x, valid_data, k=3)
                y_smooth = np.clip(spl(x_smooth), 0, None)
                ax.fill_between(x_smooth, y_smooth, 0, color=color_code, alpha=0.2)
                ax.plot(x_smooth, y_smooth, color=color_code, linewidth=line_w)
            except:
                ax.fill_between(valid_x, valid_data, 0, color=color_code, alpha=0.2)
                ax.plot(valid_x, valid_data, color=color_code, linewidth=line_w)
        else:
            ax.fill_between(valid_x, valid_data, 0, color=color_code, alpha=0.2)
            ax.plot(valid_x, valid_data, color=color_code, linewidth=line_w)

        # 최고/최저값 텍스트 표시
        if len(valid_data) > 0:
            max_idx = np.argmax(valid_data)
            min_idx = np.argmin(valid_data)
            max_val = valid_data[max_idx]
            min_val = valid_data[min_idx]
            max_x_pos = valid_x[max_idx]
            min_x_pos = valid_x[min_idx]

            disp_max = f"{int(max_val):,}"
            disp_min = f"{int(min_val):,}"

            ax.annotate(disp_max, (max_x_pos, max_val),
                        textcoords="offset points", xytext=(0, 10),
                        ha='center', fontsize=anno_fs, fontweight='bold', color=color_code)

            ax.annotate(disp_min, (min_x_pos, min_val),
                        textcoords="offset points", xytext=(0, -15),
                        ha='center', fontsize=anno_fs, fontweight='bold', color=color_code)

        # 플랫폼 이름 (하단 중앙) - 다단 배치 시 겹치지 않도록 위치 미세 조정 (-0.15)
        ax.text(0.5, -0.15, platform, transform=ax.transAxes,
                ha='center', va='top', fontsize=title_fs, fontweight='bold')

        # X축 눈금 (2019 ~ 2025)
        years = ['19', '20', '21', '22', '23', '24', '25']
        ticks = [i*4 for i in range(len(years))]
        valid_ticks = [t for t in ticks if t < len(numeric_data)]
        valid_labels = [years[i] for i, t in enumerate(ticks) if t < len(numeric_data)]

        ax.set_xticks(valid_ticks)
        ax.set_xticklabels(valid_labels, fontsize=tick_fs, color='gray')

        # Y축 범위 여유있게 (텍스트가 잘리지 않도록 1.35로 살짝 상향)
        if len(valid_data) > 0:
            y_max = np.nanmax(valid_data)
            ax.set_ylim(0, y_max * 1.35)

        # 디자인 정리 (Spine 제거)
        for spine in ax.spines.values():
            spine.set_visible(False)
        ax.tick_params(axis='x', length=0, pad=10)
        ax.set_yticks([])
        ax.set_xlim(-1, 28)

    # constrained_layout을 사용하므로 tight_layout()은 제외됨
    plt.savefig(output_path, dpi=300, bbox_inches='tight', facecolor='white')
    print(f"✅ 이미지 저장 완료: {output_path}")
    plt.show()

