# ==============================================================================
def prepare_platform_trend(df, platforms_order):
    # 플랫폼별/분기별 게시글 수 집계
    quarterly_counts = df.groupby(['Platform', 'Period']).size().unstack(fill_value=0)

    # 기간 내 모든 분기를 컬럼으로 확보 (2019Q1 ~ 2025Q3)
    start_date = "2019-01-01"
    end_date = "2025-09-30"
    all_quarters = pd.period_range(start=start_date, end=end_date, freq='Q')
    quarterly_counts = quarterly_counts.reindex(columns=all_quarters, fill_value=0)

    # 지정된 플랫폼 순서대로 정렬
    quarterly_counts = quarterly_counts.reindex(platforms_order, fill_value=0)

    return quarterly_counts

